/**
 * 타입 추론이란?
 *
 * - 타입 추론이란 타입스크립트가 코드를 해석하여 적절한 타입을 정의하는 동작을 의미한다.
 *      ex)
 *          var a = 10;
 * => 변수를 하나 선언하고 값을 할당하면 해당 변수의 타입은 자동으로 추론된다. 위 코드 a변수의 타입은 number로 정의된다.
 *
 * => 위와 같이 변수를 초기화하거나 함수의 파라미터에 기본값을 설정하거나 반환값을 설정했을 때 지정된 값을 기반으로 적당한 타입을 제시하고 정의해
 *    주는 것을 타입 추론이라고 한다.
 *
 *
 *
 */
